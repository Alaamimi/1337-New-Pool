#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int *tab;
	int i;

	if (min >= max)
		return (NULL);
	i = 0;
	if ((tab = (int *)malloc((max - min) * sizeof(int))))
	{
		while (min < max)
			tab[i++] = min++;
		return (tab);
	}
	return (tab);
}
