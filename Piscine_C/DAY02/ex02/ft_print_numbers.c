/* This exercice is quite easy, it needs nothing but 1 instruction which is
 a while loop, incrementing each value passed on to the putchar function*/
#include <unistd.h>
#include <stdlib.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	char nb;

	nb = 48;
	while (nb <= 57)
	{
		ft_putchar(nb);
		nb++;
	}
}

int	main(int argc, char **argv)
{
	(void)argc;
	(void)argv;
	ft_print_numbers();
	ft_putchar('\n');
	return (EXIT_SUCCESS);
}

