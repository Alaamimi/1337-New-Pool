/* This problem is solved easily using the if else statement, also the 
  ft_putchar function to print in stdout the following value*/
/* Also a good use of a new type of variables which is an int*/
#include <unistd.h>
#include <stdlib.h>

void	ft_putchar(char c)
{
	write(1 , &c, 1);
}

void	ft_is_negative(int n)
{
	if (n < 0)
		ft_putchar('N');
	else
		ft_putchar('P');
}

int	main(int argc, char **argv)
{
	(void)argc;
	(void)argv;
	ft_is_negative(42);
	ft_putchar('\n');
	return (EXIT_SUCCESS);
}
