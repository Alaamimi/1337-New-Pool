int	ft_sqrt(int nb)
{
	int sqrt;

	if (nb == 1)
		return (1);
	sqrt = 2;
	while ((sqrt * sqrt) <= nb && sqrt <= 46340)
	{
		if ((sqrt * sqrt) == nb)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
