#include <unistd.h>
#include <stdlib.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_ft(int *nbr)
{
	*nbr = 42;
}

void	ft_putnbr(int nb)
{
	long nbr;

	nbr = nb;
	if (nbr < 0)
	{
		nbr = -nbr;
		ft_putchar('-');
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + 48);
}

int	main(void)
{
	int *nbr;
	int n;

	n = 1;
	nbr = &n;
	ft_putnbr(n);
	ft_putchar(10);
	ft_ft(nbr);
	ft_putnbr(n);
	return(EXIT_SUCCESS);
}
